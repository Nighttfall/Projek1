    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
// SAVE PENJUALAN
java.util.Date Tanggal = this.tglPenjualan.getDate(); // Pastikan ini field-nya
String NoPenjualan = this.txtNoPenjualan.getText();
String KodeSp = this.txtKodeSp.getText();
String Qty = this.txtQty.getText();
String Diskon = this.txtDiskon.getText();
String Total = this.txtTotal.getText();

try {
    Connection c = ClassDatabase.getKoneksi();

    // Cek apakah NoPenjualan sudah ada
    String cekSql = "SELECT * FROM tbldatapenjualan WHERE NoPenjualan = ?";
    PreparedStatement cekStmt = c.prepareStatement(cekSql);
    cekStmt.setString(1, NoPenjualan);
    ResultSet rs = cekStmt.executeQuery();

    if (rs.next()) {
        JOptionPane.showMessageDialog(this, "No Penjualan sudah ada!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Insert data baru
        String sql = "INSERT INTO tbldatapenjualan (NoPenjualan, KodeSp, Qty, Tanggal, Diskon, Total) VALUES (?, ?, ?, ?, ?, ?)";
        PreparedStatement p = c.prepareStatement(sql);
        p.setString(1, NoPenjualan);
        p.setString(2, KodeSp);
        p.setString(3, Qty);
        p.setDate(4, new java.sql.Date(Tanggal.getTime()));
        p.setString(5, Diskon);
        p.setString(6, Total);
        p.executeUpdate();
        p.close();

        JOptionPane.showMessageDialog(this, "Sukses Tambah Data Penjualan");

        // Refresh tabel
        try {
            Statement s = c.createStatement();
            String sqlRefresh = "SELECT NoPenjualan, KodeSp, Qty, Tanggal, Diskon, Total FROM tbldatapenjualan";
            ResultSet r = s.executeQuery(sqlRefresh);
            tampilDataDenganHeaderCustom(); // ganti ke method tampilDataPenjualan kalau kamu pakai
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    cekStmt.close();
    rs.close();

} catch (SQLException e) {
    System.out.println("Kesalahan SQL: " + e);
}
    }//GEN-LAST:event_btnSaveActionPerformed

