public void tampilDataSparepart() {
    System.out.println("DEBUG KodeSp dari form: " + txtKodeSp.getText()); // TARUH INI

    try {
        Connection c = ClassDatabase.getKoneksi();
        String sql = "SELECT * FROM tbldatabarang WHERE KodeSp = ?";
        PreparedStatement p = c.prepareStatement(sql);
        p.setString(1, txtKodeSp.getText());
        ResultSet r = p.executeQuery();

        if (r.next()) {
            txtNamaSp.setText(r.getString("NamaSp"));
            txtHarga.setText(r.getString("HargaJual"));
            txtStok.setText(r.getString("Stok"));
        } else {
            txtNamaSp.setText("");
            txtHarga.setText("");
            txtStok.setText("");
        }

        r.close();
        p.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal ambil data sparepart: " + e.getMessage());
    }
}

private void hitungTotal() {
    try {
        int harga = Integer.parseInt(txtHarga.getText().trim());
        int qty = Integer.parseInt(txtQty.getText().trim());
        int diskon = 0;

        if (!txtDiskon.getText().trim().isEmpty()) {
            diskon = Integer.parseInt(txtDiskon.getText().trim());
        }

        int total = (harga * qty) - diskon;
        txtTotal.setText(String.valueOf(total));

    } catch (NumberFormatException e) {
        // Boleh kosongin atau kasih warning kalau mau
        txtTotal.setText("0");
    }
}
