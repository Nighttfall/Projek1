    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
// SAVE
java.util.Date TanggalPembelian = this.tglPembelian.getDate();
String NoPembelian = this.txtNoPembelian.getText();
String KodeSp = this.txtKodeSp.getText();
String JumlahBeli = this.txtJumlahBeli.getText();
String KodeSup = this.txtKodeSup.getText();

try {
    Connection c = ClassDatabase.getKoneksi();

    // Cek apakah NoPembelian sudah ada
    String cekSql = "SELECT * FROM tbldatapembelian WHERE NoPembelian = ?";
    PreparedStatement cekStmt = c.prepareStatement(cekSql);
    cekStmt.setString(1, NoPembelian);
    ResultSet rs = cekStmt.executeQuery();

    if (rs.next()) {
        // Sudah ada di database
        JOptionPane.showMessageDialog(this, "No Pembelian sudah ada!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Belum ada, lanjutkan INSERT
        String sql = "INSERT INTO tbldatapembelian (NoPembelian, KodeSp, JumlahBeli, TanggalPembelian, KodeSup) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement p = c.prepareStatement(sql);
        p.setString(1, NoPembelian);
        p.setString(2, KodeSp);
        p.setString(3, JumlahBeli);
        p.setDate(4, new java.sql.Date(TanggalPembelian.getTime()));
        p.setString(5, KodeSup);
        p.executeUpdate();
        p.close();

        JOptionPane.showMessageDialog(this, "Sukses Tambah Data");

        // Refresh tabel setelah insert
        try {
            Statement s = c.createStatement();
            String sqlRefresh = "SELECT NoPembelian, KodeSp, JumlahBeli, TanggalPembelian, KodeSup FROM tbldatapembelian";
            ResultSet r = s.executeQuery(sqlRefresh);
            tampilDataDenganHeaderCustom();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    cekStmt.close();
    rs.close();

} catch (SQLException e) {
    System.out.println("Kesalahan SQL: " + e);
}
    }//GEN-LAST:event_btnSaveActionPerformed

