    public void tampilDataDenganHeaderCustom() {
    try {
        Connection c = ClassDatabase.getKoneksi();
        Statement s = c.createStatement();
        String sql = "SELECT NoPembelian, KodeSp, JumlahBeli, TanggalPembelian, KodeSup FROM tbldatapembelian";
        ResultSet r = s.executeQuery(sql);

        // Pakai DbUtils untuk isi datanya
        tblPembelian.setModel(DbUtils.resultSetToTableModel(r));

        // Ganti nama header langsung
        String[] namaKolom = {
            "No Pembelian",
            "Kode Sparepart",
            "Jumlah Beli",
            "Tanggal Pembelian",
            "Kode Supplier"
        };

        for (int i = 0; i < namaKolom.length; i++) {
            tblPembelian.getColumnModel().getColumn(i).setHeaderValue(namaKolom[i]);
        }

        tblPembelian.getTableHeader().repaint();
        r.close();
        s.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
    
public void tampilDataSparepart() {
    System.out.println("DEBUG KodeSp dari form: " + txtKodeSp.getText()); // TARUH INI

    try {
        Connection c = ClassDatabase.getKoneksi();
        String sql = "SELECT * FROM tbldatabarang WHERE KodeSp = ?";
        PreparedStatement p = c.prepareStatement(sql);
        p.setString(1, txtKodeSp.getText());
        ResultSet r = p.executeQuery();

        if (r.next()) {
            txtNamaSp.setText(r.getString("NamaSp"));
            txtHargaBeli.setText(r.getString("HargaBeli"));
            txtHargaJual.setText(r.getString("HargaJual"));
            txtTotalStok.setText(r.getString("Stok"));
        } else {
            txtNamaSp.setText("");
            txtHargaBeli.setText("");
            txtHargaJual.setText("");
            txtTotalStok.setText("");
        }

        r.close();
        p.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal ambil data sparepart: " + e.getMessage());
    }
}

public void tampilDataSupplier() {
    System.out.println("DEBUG KodeSup dari form: " + txtKodeSup.getText()); // TARUH INI

    try {
        Connection c = ClassDatabase.getKoneksi();
        String sql = "SELECT * FROM tbldatasup WHERE KodeSup = ?";
        PreparedStatement p = c.prepareStatement(sql);
        
        if (!txtKodeSup.getText().isEmpty()) {
            p.setInt(1, Integer.parseInt(txtKodeSup.getText())); // INT bukan String!
        } else {
            return;
        }

        ResultSet r = p.executeQuery();

        if (r.next()) {
            txtNamaSup.setText(r.getString("NamaSup"));
        } else {
            txtNamaSup.setText("");
        }

        r.close();
        p.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal ambil data supplier: " + e.getMessage());
    }
}
